static struct nesbus *bus;
static struct cpu6502 *cpu;

int
main(int argc, char *argv[] )
{
	( void ) argc;
	( void ) argv;
	uint32_t INST_CNT = 200;

	struct nes_cartridge *cartridge;

	uint8_t buf[0x100];

	printf( "Emu version %d.%d\n", emu_VERSION_MAJOR, emu_VERSION_MINOR );
	
	cartridge = load_rom( "/mnt/c/work/test/roms/nestest.nes" );
	cartridge_info( cartridge );

	bus = nesbus_init( );
	cpu = cpu6502_init( bus );
	bus->connect_cartridge( cartridge );


	printf("End of the cartridge:\n");
	bus->debug_read(0xffff-0xf, buf, 0x10);
	hex_dump(buf, 0x10);

	printf("PC:\n");
	bus->debug_read(cpu->PC, buf, 0x20);
	hex_dump( buf, 0x20 );
	cpu->reset();

	do
	{
		printf("Instruction # %d\n", 200-INST_CNT);
		cpu->fetch( );
		cpu->decode( );
		printf("%04x: %02x %s %04x / %02x\n",
				cpu->start_pc,
				cpu->opcode,
				cpu->curr_insn->mnem,
				cpu->operand_addr,
				cpu->operand);

		cpu->execute( );
		cpu->print_regs( );
		bus->debug_read(0x200-0x10, buf, 0x20);
		printf("Stack:\n");
		hex_dump( buf, 0x20 );
		bus->debug_read(cpu->PC, buf, 0x10);
		printf("%04x: \n",cpu->PC);
		hex_dump( buf, 0x10 );
		bus->debug_read(0, buf, 0x20);
		printf("%04x: \n",0);
		hex_dump( buf, 0x20 );
		printf("\n");
	} while ( INST_CNT-- );//cpu->PC < sizeof( SAMPLE_BIN ) );

	return 0;
}